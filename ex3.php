<?php
// =======================
// 1. Вартість товарів
// =======================

// Створюємо змінні для зберігання вартості товарів
$product1 = 200;
$product2 = 150;
$product3 = 250;

// Обчислюємо загальну вартість
$total = $product1 + $product2 + $product3;

// Виводимо результат
echo "Загальна вартість покупки: " . $total . " грн<br>";


// =======================
// 2. Масив фільмів
// =======================

// Створюємо масив улюблених фільмів
$movies = ["Губка БОБ", "Той хто біжить по лабіринту", "Інтерстелар", "Джуманджі"];

// Виводимо фільми за допомогою foreach
echo "<br>Улюблені фільми:<br>";
foreach ($movies as $movie) {
    echo $movie . "<br>";
}


// =======================
// 3. Асоціативний масив користувача
// =======================

// Створюємо асоціативний масив
$user = [
    "login" => "admin",
    "password" => "12345",
    "email" => "admin@example.com"
];

// Виводимо значення
echo "<br>Дані користувача:<br>";
echo "Логін: " . $user["login"] . "<br>";
echo "Пароль: " . $user["password"] . "<br>";
echo "Email: " . $user["email"] . "<br>";


// =======================
// 4. Знижка
// =======================

// Перевіряємо, чи перевищує сума 500 грн
if ($total > 500) {
    $discount = 0.1; // 10% знижка
    $finalTotal = $total - ($total * $discount);
    echo "<br>З урахуванням знижки: " . $finalTotal . " грн<br>";
} else {
    echo "<br>Знижка не застосовується<br>";
}


// =======================
// 5. Перевірка логіну і паролю
// =======================

// Правильні дані
$correctLogin = "admin";
$correctPassword = "12345";

// Вхідні дані (можна змінити для перевірки)
$inputLogin = "admin";
$inputPassword = "12345";

// Перевірка за допомогою логічних операторів
if ($inputLogin === $correctLogin && $inputPassword === $correctPassword) {
    echo "<br>Вхід успішний!";
} else {
    echo "<br>Невірний логін або пароль!";
}

?>