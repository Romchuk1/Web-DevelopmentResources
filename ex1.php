<?php
// ================================
// 1. Створення змінних і вивід
// ================================

// Оголошення змінних
$name = "Марк";           // Ім'я користувача
$age = 80;               // Вік користувача
$is_student = true;      // Чи є студентом (логічна змінна)

// Вивід значень у вигляді речення
echo "Мене звати $name, мені $age років. ";
echo $is_student ? "Я є студентом.<br>" : "Я не є студентом.<br>";


// ================================
// 2. Масив чисел і сума елементів
// ================================

// Створення масиву від 1 до 5
$numbers = [1, 2, 3, 4, 5];

// Знаходження суми
$sum = array_sum($numbers); // обчислення суми

echo "Сума елементів масиву: $sum <br>";


// ================================
// 3. Асоціативний масив і HTML список
// ================================

// Створення асоціативного масиву
$user = [
    "name" => "Марк",
    "email" => "mark@example.com",
    "phone" => "+380123456789"
];

// Вивід у вигляді HTML списку
echo "<ul>";
foreach ($user as $key => $value) {
    echo "<li>$key: $value</li>";
}
echo "</ul>";


// ================================
// 4. Перевірка віку (if)
// ================================

// Перевірка чи більше 18
if ($age > 18) {
    echo "Користувач повнолітній.<br>";
} else {
    echo "Користувач неповнолітній.<br>";
}


// ================================
// 5. Оцінка за шкалою
// ================================

// Оголошення змінної оцінки
$grade = 85;

// Перевірка діапазонів
if ($grade >= 90 && $grade <= 100) {
    echo "Оцінка: Відмінно";
} elseif ($grade >= 70) {
    echo "Оцінка: Добре";
} elseif ($grade >= 50) {
    echo "Оцінка: Задовільно";
} else {
    echo "Оцінка: Незадовільно";
}

?>