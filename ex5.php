<?php
// =======================
// 1. Ім’я та вік
// =======================

// Задаємо змінні
$first_name = "Марк";
$last_name = "Маркевич";
$year_of_birth = 2005;

// Отримуємо поточний рік
$current_year = date("Y");

// Обчислюємо вік
$age = $current_year - $year_of_birth;

// Виводимо повне ім’я та вік
echo "Повне ім’я: " . $first_name . " " . $last_name . "<br>";
echo "Вік: " . $age . "<br>";


// =======================
// 2. Масив країн (HTML список)
// =======================

// Створюємо масив країн
$countries = ["Україна", "Польща", "Китай", "Італія"];

// Виводимо нумерований список
echo "<br>Список країн:<br>";
echo "<ol>";
foreach ($countries as $country) {
    echo "<li>" . $country . "</li>";
}
echo "</ol>";


// =======================
// 3. Міста і населення
// =======================

// Асоціативний масив
$cities = [
    "Луцьк" => 3000000,
    "Звірів" => 72000000,
    "Одеса" => 110000,
    "Харків" => 1402400
];

// Виводимо міста з населенням більше 1 млн
echo "<br>Міста з населенням > 1 млн:<br>";
foreach ($cities as $city => $population) {
    if ($population > 1000000) {
        echo $city . " - " . $population . "<br>";
    }
}


// =======================
// 4. Парне чи непарне
// =======================

// Задаємо число
$number = 8;

// Перевірка
if ($number % 2 == 0) {
    echo "<br>Число $number - Парне<br>";
} else {
    echo "<br>Число $number - Непарне<br>";
}


// =======================
// 5. Високосний рік
// =======================

// Отримуємо поточний рік
$year = date("Y");

// Перевірка
if ($year % 4 == 0) {
    echo "<br>$year рік є високосним";
} else {
    echo "<br>$year рік не є високосним";
}


?>