<?php
// ================================
// 1. Арифметичні операції
// ================================

// Оголошення змінних
$a = 5;
$b = 10;

// Обчислення
$sum = $a + $b;        // Сума
$diff = $a - $b;       // Різниця
$product = $a * $b;    // Добуток
$division = $a / $b;   // Ділення

// Вивід результатів
echo "Сума: $sum <br>";
echo "Різниця: $diff <br>";
echo "Добуток: $product <br>";
echo "Ділення: $division <br>";


// ================================
// 2. Масив днів тижня
// ================================

// Створення масиву
$days = ["Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday", "Sunday"];

// Вивід 3-го і 5-го дня (індексація з 0)
echo "3-й день: " . $days[2] . "<br>";
echo "5-й день: " . $days[4] . "<br>";


// ================================
// 3. Асоціативний масив товарів
// ================================

// Створення масиву
$products = [
    "ПК" => 25000,
    "Саня" => 10,
    "Рома" => 200000000000000000000000,
];

// Вивід товарів і цін
foreach ($products as $name => $price) {
    echo "Товар: $name — Ціна: $price грн <br>";
}


// ================================
// 4. Оператор switch
// ================================

// Змінна дня
$day = "Monday";

// Вибір дії
switch ($day) {
    case "Monday":
        echo "Початок робочого тижня <br>";
        break;
    case "Friday":
        echo "Майже вихідні! <br>";
        break;
    case "Saturday":
    case "Sunday":
        echo "Вихідний день <br>";
        break;
    default:
        echo "Звичайний робочий день <br>";
}


// ================================
// 5. Парне чи непарне число
// ================================

// Оголошення змінної
$x = 15;

// Перевірка
if ($x % 2 == 0) {
    echo "Число $x є парним";
} else {
    echo "Число $x є непарним";
}

?>