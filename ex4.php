<?php
// =======================
// 1. Максимум і мінімум
// =======================

// Задаємо дві змінні
$a = 40;
$b = 55;

// Знаходимо максимум і мінімум через умовний оператор
if ($a > $b) {
    $max = $a;
    $min = $b;
} else {
    $max = $b;
    $min = $a;
}

// Виводимо результат
echo "Максимум: " . $max . "<br>";
echo "Мінімум: " . $min . "<br>";


// =======================
// 2. Середнє арифметичне
// =======================

// Створюємо масив чисел
$numbers = [10, 20, 30, 40, 50];

// Обчислюємо суму елементів
$sum = 0;
foreach ($numbers as $num) {
    $sum += $num;
}

// Обчислюємо середнє арифметичне
$average = $sum / count($numbers);

// Виводимо результат
echo "<br>Середнє арифметичне: " . $average . "<br>";


// =======================
// 3. Студенти і бали
// =======================

// Асоціативний масив: ПІБ => середній бал
$students = [
    "Марк Маркович" => 83,
    "Хтось там" => 67,
    "Там хтось" => 20
];

// Виводимо студентів із балом більше 80
echo "<br>Студенти з балом більше 80:<br>";
foreach ($students as $name => $grade) {
    if ($grade > 80) {
        echo $name . " - " . $grade . "<br>";
    }
}


// =======================
// 4. Перевірка кратності
// =======================

// Задаємо число
$num = 12;

// Перевіряємо кратність 3 або 5
if ($num % 3 == 0 || $num % 5 == 0) {
    echo "<br>Число $num кратне 3 або 5";
} else {
    echo "<br>Число $num не кратне ні 3, ні 5";
}

?>